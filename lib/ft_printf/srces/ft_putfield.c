#include "../includes/libprintf.h"

void		ft_putfield(char *s, t_list *lst)
{
	int		i;

	i = 0;
	while (s[i])
		i++;
	while (i < lst->field)
	{
		ft_putchar(' ');
		i++;
	}
}
